export function getStoredLanguage() {
  try {
    const user = JSON.parse(localStorage.getItem('user') || '{}');
    return user.language || localStorage.getItem('language') || 'en';
  } catch (_) {
    return localStorage.getItem('language') || 'en';
  }
}

export function t(key, language = getStoredLanguage()) {
  return DICTIONARY[language]?.[key] || DICTIONARY.en[key] || key;
}

export function translateServerMessage(message, language = getStoredLanguage()) {
  if (!message) return '';
  const original = String(message);
  const exactMap = {
    'User not found': 'user_not_found',
    'Please confirm your email first': 'confirm_email_first',
    'Password is required': 'password_required',
    'Invalid or expired token': 'invalid_or_expired_token',
    'Unsupported language': 'unsupported_language',
    'Username, email and password are required':
      'username_email_password_required',
    'Username must contain only letters and be 2-10 characters long':
      'username_invalid',
    'Display name must contain only letters and be 2-10 characters long':
      'display_name_invalid',
    'Enter a valid email address': 'enter_valid_email',
    'Password must be at least 6 characters long': 'password_invalid',
    'Email already exists': 'email_exists',
    'Username already exists': 'username_exists',
    'Login and password are required': 'login_password_required',
    'Invalid login or password': 'invalid_login_password',
    'Please check your mailbox and confirm your email before login':
      'confirm_email_before_login',
    'Login error': 'login_error',
    'Email is already verified': 'email_already_verified',
    'Verification email sent. Please check your mailbox.':
      'verification_email_sent',
    'Error resending verification email': 'resend_error',
  };

  if (exactMap[original]) {
    return t(exactMap[original], language);
  }

  const lower = original.toLowerCase();

  if (lower.includes('confirm') && lower.includes('email')) {
    return t('confirm_email_before_login', language);
  }
  if (lower.includes('unsupported language')) {
    return t('unsupported_language', language);
  }

  return message;
}
